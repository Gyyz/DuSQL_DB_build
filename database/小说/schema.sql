PRAGMA foreign_keys = ON;

CREATE TABLE "作者" (
"词条id" int,
"姓名" text,
"国籍" text,
"出生日期" time,
"出生地" text,
"逝世日期" time,
"作品数量" int,
primary key ("词条id")
);

CREATE TABLE "小说" (
"词条id" int,
"小说名" text,
"文学体裁" text,
"作者id" int,
"首版时间" time,
"字数" int,
primary key ("词条id"),
foreign key("作者id") references "作者"("词条id")
);

CREATE TABLE "小说改编话剧" (
"词条id" int,
"名称" text,
"小说id" int,
"演出剧团" text,
"导演" text,
"演出总场次" int,
"观众评分" int,
primary key ("词条id"),
foreign key("小说id") references "小说"("词条id")
);

CREATE TABLE "小说改编电视剧" (
"词条id" int,
"剧名" text,
"小说id" int,
"首播时间" time,
"集数" int,
"豆瓣评分" int,
primary key ("词条id"),
foreign key("小说id") references "小说"("词条id")
);

