CREATE TABLE "电商" (
"词条id" int,
"名称" text,
"所属公司" text,
"类型" text,
"影响力指数" int,
"排名" int,
"上线国家数量" int,
primary key ("词条id")
);

CREATE TABLE "电商覆盖国家" (
"电商id" int,
"支持国家" text,
"上线时间" time,
"负责的分公司名称" text,
foreign key("电商id") references "电商"("词条id")
);

CREATE TABLE "商家" (
"词条id" int,
"名称" text,
"所属国家" text,
"主营类别" text,
"网店覆盖国家数量" int,
"实体店数量" int,
"入驻电商数量" int,
primary key ("词条id")
);

CREATE TABLE "商家网点覆盖国家" (
"商家id" int,
"国家" text,
"实体店数量" int,
"年平均营业额" int,
foreign key("商家id") references "商家"("词条id")
);

CREATE TABLE "商家交易额" (
"商家id" int,
"电商平台id" int,
"上线品类数量" int,
"促销品类数量" int,
"平均折扣" int,
"交易额" int,
"占全年销售额比例" int,
foreign key("电商平台id") references "电商"("词条id"),
foreign key("商家id") references "商家"("词条id")
);

CREATE TABLE "电商活动交易额" (
"电商平台id" int,
"活动日" text,
"参加活动商家数量" int,
"促销商品数量" int,
"交易额" int,
foreign key("电商平台id") references "电商"("词条id")
);

