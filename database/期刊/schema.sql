PRAGMA foreign_keys = ON;

CREATE TABLE "期刊" (
"词条id" int,
"名称" text,
"语言" text,
"类别" text,
"主办单位" text,
"创刊时间" time,
"国家" text,
"出版刊数" int,
primary key ("词条id")
);

CREATE TABLE "期刊文献" (
"年份" time,
"期刊id" int,
"统计平台" text,
"出版文献数" int,
"被下载数量" int,
"被引数量" int,
"复合影响因子" int,
"综合影响因子" int,
foreign key("期刊id") references "期刊"("词条id")
);

CREATE TABLE "封面人物" (
"词条id" int,
"姓名" text,
"职业" text,
primary key ("词条id")
);

CREATE TABLE "期刊封面人物" (
"人物id" int,
"期刊id" int,
"次数" int,
foreign key("人物id") references "封面人物"("词条id"),
foreign key("期刊id") references "期刊"("词条id")
);

